// scripts/generate-tariff-files.cjs
// Run once: node scripts/generate-tariff-files.cjs
// Splits js/tariffs.js into per-state files under js/tariffs/
'use strict';
const fs = require('fs');
const path = require('path');

function slugify(name) {
  return name.toLowerCase()
    .replace(/&/g, 'and')
    .replace(/[^a-z0-9\s-]/g, '')
    .replace(/\s+/g, '-')
    .replace(/-+/g, '-')
    .trim();
}

function serialize(v, depth) {
  const pad = '  '.repeat(depth);
  const padIn = '  '.repeat(depth + 1);

  if (v === null) return 'null';
  if (v === undefined) return 'undefined';
  if (v === Infinity) return 'Infinity';
  if (typeof v === 'number') return String(v);
  if (typeof v === 'boolean') return String(v);
  if (typeof v === 'string') return JSON.stringify(v);

  if (Array.isArray(v)) {
    if (v.length === 0) return '[]';
    const items = v.map(item => padIn + serialize(item, depth + 1)).join(',\n');
    return '[\n' + items + '\n' + pad + ']';
  }

  if (typeof v === 'object') {
    const keys = Object.keys(v);
    if (keys.length === 0) return '{}';
    const ident = /^[a-zA-Z_$][a-zA-Z0-9_$]*$/;
    const pairs = keys.map(k => {
      const safe = ident.test(k) ? k : JSON.stringify(k);
      return padIn + safe + ': ' + serialize(v[k], depth + 1);
    }).join(',\n');
    return '{\n' + pairs + '\n' + pad + '}';
  }
  return JSON.stringify(v);
}

// ── Load TARIFF_DB ────────────────────────────────────────────────────────────
const rootDir = path.join(__dirname, '..');
const src = fs.readFileSync(path.join(rootDir, 'js', 'tariffs.js'), 'utf8');
const helperStart = src.indexOf('\n// Helper: get sorted');
const dbSection = src.substring(0, helperStart).trim();
const tempFile = path.join(rootDir, '_tmp_extract.cjs');
fs.writeFileSync(tempFile, dbSection.replace('const TARIFF_DB = ', 'module.exports = ') + ';\n');
const db = require(tempFile);
fs.unlinkSync(tempFile);

// ── Create output directory ───────────────────────────────────────────────────
const outDir = path.join(rootDir, 'js', 'tariffs');
fs.mkdirSync(outDir, { recursive: true });

// ── Write per-state files ─────────────────────────────────────────────────────
const stateFiles = [];
for (const [state, discoms] of Object.entries(db)) {
  const slug = slugify(state);
  stateFiles.push({ state, slug });

  const content = [
    '// ' + state + ' — Electricity Tariff Data (2024-25)',
    '// Source: Publicly available tariff orders from the respective SERC.',
    '// To update rates: edit energySlabs, fixedCharge, or additionalCharges below.',
    '// See TARIFF_GUIDE.md for the complete field schema and step-by-step instructions.',
    '',
    'export default {',
    '  state: ' + JSON.stringify(state) + ',',
    '  discoms: ' + serialize(discoms, 1),
    '};',
    '',
  ].join('\n');

  fs.writeFileSync(path.join(outDir, slug + '.js'), content);
  console.log('  Created js/tariffs/' + slug + '.js  (' + discoms.length + ' DISCOM' + (discoms.length > 1 ? 's' : '') + ')');
}

// ── Write registry.js ─────────────────────────────────────────────────────────
const varName = s => '_' + s.replace(/-/g, '_');
const importLines = stateFiles.map(f => 'import ' + varName(f.slug) + " from './" + f.slug + ".js';").join('\n');
const itemList = stateFiles.map(f => '  ' + varName(f.slug)).join(',\n');

const registry = `// js/tariffs/registry.js
// Auto-generated aggregator — imports all per-state files into a single TARIFF_DB.
// Do NOT edit this file directly; update the individual state files instead.

${importLines}

export const TARIFF_DB = {};
[
${itemList}
].forEach(m => { TARIFF_DB[m.state] = m.discoms; });

export function getStates() { return Object.keys(TARIFF_DB).sort(); }

export function getDiscoms(state) { return TARIFF_DB[state] || []; }

export function findDiscom(discomId) {
  for (const state of Object.values(TARIFF_DB)) {
    const d = state.find(x => x.id === discomId);
    if (d) return d;
  }
  return null;
}

export function getCategories(discomId) {
  const discom = findDiscom(discomId);
  return discom ? discom.categories : [];
}

export function getCategory(discomId, categoryId) {
  const cats = getCategories(discomId);
  return cats.find(c => c.id === categoryId) || null;
}

export function getSupplyTypes(discomId, categoryId) {
  const cat = getCategory(discomId, categoryId);
  return (cat && cat.supplyTypes) ? cat.supplyTypes : [];
}

export function getEffectiveTariff(discomId, categoryId, supplyTypeId) {
  const cat = getCategory(discomId, categoryId);
  if (!cat) return null;
  if (cat.supplyTypes && cat.supplyTypes.length > 0) {
    const st = supplyTypeId
      ? (cat.supplyTypes.find(s => s.id === supplyTypeId) || cat.supplyTypes[0])
      : cat.supplyTypes[0];
    return { ...st, categoryId: cat.id, categoryName: cat.name };
  }
  return { ...cat, categoryId: cat.id, categoryName: cat.name };
}
`;

fs.writeFileSync(path.join(outDir, 'registry.js'), registry);
console.log('  Created js/tariffs/registry.js  (' + stateFiles.length + ' states)');
console.log('\nDone. ' + stateFiles.length + ' state files + 1 registry = ' + (stateFiles.length + 1) + ' files total.');
