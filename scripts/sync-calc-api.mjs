// scripts/sync-calc-api.mjs — stage the bill engine into the calc Edge Function.
//
// Supabase's bundler can't import from arbitrary domains, so the pure engine modules
// are COPIED (never edited) from js/ into calc/vendor/, then esbuild flattens the
// function to a single bundle.ts that can be pasted into the dashboard's function
// editor. vendor/ and bundle.ts are git-ignored: js/ stays the single source of truth.
//
// Run via:  npm run api:bundle
// Deploy:   paste supabase/functions/calc/bundle.ts into the dashboard editor
//           (function name: calc, JWT verification OFF — public endpoint),
//           or with the CLI: supabase functions deploy calc --no-verify-jwt

import { cpSync, mkdirSync, rmSync, writeFileSync } from 'fs';
import { execSync } from 'child_process';
import { dirname, join } from 'path';
import { fileURLToPath } from 'url';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const fn = join(root, 'supabase', 'functions', 'calc');
const vendor = join(fn, 'vendor');

rmSync(vendor, { recursive: true, force: true });
mkdirSync(vendor, { recursive: true });

cpSync(join(root, 'js', 'engine.js'), join(vendor, 'engine.js'));
cpSync(join(root, 'js', 'utils.js'), join(vendor, 'utils.js'));
cpSync(join(root, 'js', 'tariffs'), join(vendor, 'tariffs'), { recursive: true });

writeFileSync(join(vendor, 'README.md'),
  'Generated by scripts/sync-calc-api.mjs — do not edit. Source of truth: /js.\n');

execSync(`npx --yes esbuild "${join(fn, 'index.ts')}" --bundle --format=esm --platform=neutral --outfile="${join(fn, 'bundle.ts')}"`,
  { stdio: 'inherit' });

console.log('calc API bundle ready:', join(fn, 'bundle.ts'));
